@Injectable({
  providedIn: 'root'
})
export class I18nService {
  private readonly storageKey = 'barberapp.language';
  private readonly languageSubject = new BehaviorSubject<AppLanguage>(this.getInitialLanguage());

  readonly language$ = this.languageSubject.asObservable();

  get language(): AppLanguage {
    return this.languageSubject.value;
  }

  setLanguage(language: AppLanguage): void {
    this.languageSubject.next(language);
    localStorage.setItem(this.storageKey, language);
  }

  toggleLanguage(): void {
    this.setLanguage(this.language === 'en' ? 'sr' : 'en');
  }

  t(key: string): string {
    const current = TRANSLATIONS[this.language][key];
    if (current) {
      return current;
    }

    return TRANSLATIONS.en[key] ?? key;
  }

  /** Maps known API / Identity messages (EN or SR) to the active UI language. */
  localizeMessage(raw: string | null | undefined, fallbackKey = 'common.genericError'): string {
    if (!raw || !raw.trim()) {
      return this.t(fallbackKey);
    }

    const parts = raw
      .split(/\n|,(?=\s*Passwords must)|,(?=\s*Lozinka mora)/)
      .map((part) => part.trim())
      .filter(Boolean);

    if (parts.length > 1) {
      return parts.map((part) => this.localizeMessage(part, fallbackKey)).join('\n');
    }

    const normalized = raw.trim();
    const key = API_MESSAGE_KEYS[normalized];
    if (key) {
      return this.t(key);
    }

    return normalized;
  }

  private getInitialLanguage(): AppLanguage {
    const stored = localStorage.getItem(this.storageKey);
    if (stored === 'en' || stored === 'sr') {
      return stored;
    }

    return 'en';
  }
}
